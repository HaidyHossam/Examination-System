    private void StartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartActionPerformed
        // TODO add your handling code here:
        Date late = new Date(119, ES.getCurrentExam().getDate().getMonth(), ES.getCurrentExam().getDate().getDate(),
                ES.getCurrentExam().getDate().getHours(), ES.getCurrentExam().getDate().getMinutes() + 30, 00);

        Date now = new java.util.Date();

        SimpleDateFormat formatter = new SimpleDateFormat("HH:mm:ss");
        formatter.format(now);

        if (now.before(ES.getCurrentExam().getDate())) {
            JOptionPane.showMessageDialog(jTabbedPane1, "Exam hasn't started yet", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (now.after(ES.getCurrentExam().getDate()) || now.equals(ES.getCurrentExam().getDate())) {
            if (now.before(late)) {
                LogInOrRegister.check = false;
                CurrentExam C = new CurrentExam(ES);
                C.show();
                S_CurrentExam_pnl.hide();
            } else if (now.after(late)) {
                JOptionPane.showMessageDialog(jTabbedPane1, "Access Denied", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_StartActionPerformed

    private void RefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshActionPerformed
        // TODO add your handling code here:
        if (ES != (null)) {
            if (LogInOrRegister.check == true) {
                S_ExamTitle_lbl.setText(ES.getCurrentExam().getCourseID() + " Exam");
                S_SDate_lbl.setText("Start Date:" + ES.getCurrentExam().getDate());
                S_Duration_lbl.setText("Duration: " + ES.getCurrentExam().getDuration() + " Hour(s)");
                S_CurrentExam_pnl.show();
            }
        } else {
            JOptionPane.showMessageDialog(jTabbedPane1, "No new Exams", "INFO", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_RefreshActionPerformed

