    public Studentt(Student s) {
        initComponents();
        Exams = new ArrayList<Exam>();
        students = new ArrayList<String>();
        examsessions = new ArrayList<ExamSession>();
        Date now = new java.util.Date();

        ES = new ExamSession();
        this.s = s;
        String[] exams = ExamSession.Spliting();
        if (exams.length > 0) {
            ES = new ExamSession();
            for (int i = 0; i < exams.length; i++) {
                Exams.add(ExamSession.Get_ExamInfo(exams[i]));
                ExamSession S = ES.Assign_Students(Exams.get(i));
                examsessions.add(S);
            }

            for (ExamSession examsession : examsessions) {
                students = examsession.getStud_UserName();
                Date EndDate = new Date(examsession.getCurrentExam().getDate().getYear()
                        , examsession.getCurrentExam().getDate().getMonth(), examsession.getCurrentExam().getDate().getDate()
                        , (examsession.getCurrentExam().getDate().getHours() + examsession.getCurrentExam().getDuration())
                        , examsession.getCurrentExam().getDate().getMinutes(), 0);
                for (String std : students) {
                    if (std.equals(s.getUserName())) {
                        if (!EndDate.before(now)) {
                            ES = examsession;
                            break;
                        } else {
                            ES = null;
                        }
                    }
                }
            }
        }
        S_Name_txt.setText(s.getName());
        S_UserName_txt.setText(s.getUserName());

        S_CurrentExam_pnl.hide();

    }

